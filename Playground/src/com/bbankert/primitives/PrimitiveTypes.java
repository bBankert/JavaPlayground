package com.bbankert.primitives;

public class PrimitiveTypes {

	public static void main(String[] args) {
		byte b = 1;
		short s;
		s = 2;
		int i = 3;
		long l = 4;
		char c = 'a';
		
		boolean bool = true;
		float f = 1.2F;
		
		double d = 1.2;
		// underscore can be used to represent "commas"
		long l2 = 2_000_000_000;
		
		// var is an implicit type
		var v = 1;

		byte b2 = (byte)128;
	}

}
